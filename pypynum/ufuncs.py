def add(x, y):
    return x + y


def subtract(x, y):
    return x - y


def multiply(x, y):
    return x * y


def divide(x, y):
    return x / y


def floor_divide(x, y):
    return x // y


def modulo(x, y):
    return x % y


def power(x, y, m=None):
    return pow(x, y, m)
